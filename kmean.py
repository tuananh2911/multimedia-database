# Tạo mảng numpy từ dữ liệu
feature_list = []
file_segments = []
file_names = []

for item in data:
    file_name = item["file_name"]
    file_names.append(file_name)
    for segment in item["file_results"]:
        features = [
            segment['bandwidth'],
            segment['harmonicity'],
            segment['pitch'],
            segment['average_energy'],
            segment['zero_crossing_rate'],
            segment['silence_percentage']
        ]
        features.extend(segment['mfccs'])
        feature_list.append(features)
        file_segments.append(file_name)

X = np.array(feature_list)

# Chuẩn hóa dữ liệu
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# Áp dụng K-means
kmeans = KMeans(n_clusters=2, random_state=42)
kmeans.fit(X_scaled)

# Lưu mô hình K-means và scaler
with open('kmeans_model.pkl', 'wb') as file:
    pickle.dump(kmeans, file)

with open('scaler.pkl', 'wb') as file:
    pickle.dump(scaler, file)

# Xác định cụm chiếm ưu thế trong mỗi file
labels = kmeans.labels_
file_cluster_count = {file_name: [] for file_name in file_names}
for label, file_name in zip(labels, file_segments):
    file_cluster_count[file_name].append(label)

file_cluster = []
for file_name, clusters in file_cluster_count.items():
    most_common_cluster = Counter(clusters).most_common(1)[0][0]
    file_cluster.append({"filename": file_name, "cluster": most_common_cluster})

